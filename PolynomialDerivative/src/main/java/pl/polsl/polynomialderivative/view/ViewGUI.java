    private void acceptBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptBtnActionPerformed
        errorLbl.setText("");
        try {
            if (Integer.parseInt(degreeTxt.getText()) < 0) {
                throw new NegatvieNumber("");
            }
            int size = Integer.parseInt(degreeTxt.getText()) + 1;
            Object[][] rows = new Object[1][size];
            for (int i = 0; i < size; i++) {
                rows[0][i] = 0;
            }
            String[] titles = new String[size];
            int counter = size - 1;
            for (int i = 0; i < size; i++) {
                titles[i] = "x^" + Integer.toString(counter--);
            }
            factorsTable.setModel(new javax.swing.table.DefaultTableModel(
                    rows,
                    titles
            ) {
                @Override
                public Class getColumnClass(int columnIndex) {
                    return java.lang.Double.class;
                }
            });
        } catch (java.lang.NumberFormatException e) {
            errorLbl.setText("Enter integer!");
        } catch (NegatvieNumber e) {
            errorLbl.setText("Enter POSITIVE integer!");
        }
    }//GEN-LAST:event_acceptBtnActionPerformed


