    public int getDegree() {
        int degree = 0;
        try {
            degree = Integer.parseInt(degreeTxt.getText());

        } catch (java.lang.NumberFormatException e) {
            errorLbl.setText("Enter integer!");
        }
        return degree;
    }

    public ArrayList<Double> getFactors() {
        ArrayList<Double> factors = new ArrayList<>();
        try {
            for (int i = 0; i <= getDegree(); i++) {
                factors.add(Double.parseDouble((factorsTable.getModel().getValueAt(0, i)).toString()));
            }
        } catch (java.lang.ArrayIndexOutOfBoundsException e) {
            errorLbl.setText("Accept degree first to enter factors!");
        }
        return factors;
    }

